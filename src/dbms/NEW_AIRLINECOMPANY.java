    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String ab=jTextField2.getText();
        if(ab.isEmpty()){
            JOptionPane.showMessageDialog(null, "PLEASE ENTER THE AIRLINEID");
        }
        else{
         String sql="select * from AIRLINECOMPANY where AIRLINEID=?";
        try{
         pst=conn.prepareStatement(sql);
         pst.setString(1, jTextField2.getText());
         rs=pst.executeQuery();
         if(rs.next()){
             String add1=rs.getString("NAME");
             jTextField1.setText(add1);
             String add2=rs.getString("IATA");
             jTextField3.setText(add2);
             String add3=rs.getString("ICAO");
             jTextField4.setText(add3);
             
            
             rs.close();
             pst.close();
         }else{
             JOptionPane.showMessageDialog(null, "AIRLINEID NOT FOUND");
         }
    }catch(Exception e){
               JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

